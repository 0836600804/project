    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        frm_panel_PhieuNhap pn = new frm_panel_PhieuNhap(this);
        int index = (tab.getTabCount() + 1);
        if (index <= 10) {
            if (checkPhieuNhapMax3()) {
                tab.addTab("Phiếu Nhập " + countPhieuNhap + "  ", getIScaleImage("src\\BHX_IMG\\folderImages\\images\\icons\\page_nh.png", 20, 20), pn, "Khởi tạo phiếu nhập hàng !");
                countPhieuNhap++;
                return;
            }
            JOptionPane.showMessageDialog(this, "Bạn chỉ được phép làm việc với 3 Tab phiếu nhập !");
            return;
        }
        JOptionPane.showMessageDialog(this, "Bạn chỉ được phép tạo tối đa 10 Tab !");
    }//GEN-LAST:event_btnAddActionPerformed

