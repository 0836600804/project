    private boolean checkPhieuNhapMax3() {
        int count = 1;
        frm_panel_PhieuNhap pn = new frm_panel_PhieuNhap(this);
        for (int i = 0; i < tab.getTabCount(); i++) {
            try {
                frm_panel_PhieuNhap pN = (frm_panel_PhieuNhap) tab.getTabComponentAt(i);
                count++;
            } catch (Exception e) {
            }
        }
        if (count <= 3) {
            return true;
        }
        return false;
    }

    private ImageIcon getIScaleImage(String url, int w, int h) {
        ImageIcon icon = new ImageIcon(url);
        Image img = icon.getImage();
        Image scale = img.getScaledInstance(w, h, Image.SCALE_SMOOTH);
        ImageIcon scalIcon = new ImageIcon(scale);
        return scalIcon;
    }


